print("For Loop In Python")
print("Example 1:-")
for a in "Python":
    print(a)
print("Example 2:-")
for b in ['Ayush', 'Ragini', 'Anil', 'Manju']:
    print(b)
print("Example 3:-")
for c in [1,2,3,4,5]:
    print(c)
print("Example 4:-")
for d in range(10):
    print(d)
print("Example 5:-")
for e in range(5,20):
    print(e)
print("Example 6:-")
for f in range(5,20,2):
    print(f)